
public class TutorialVariables {

	public static void main(String[] args) {
		//String is a text/characters. This is also how you assign a String variable in Java.
		String firstName = "Gavin";
		String lastName = "Nguyen";
		// You will notice in this line of code that we added "+" signs. this is called concatenating 
		System.out.println("Hi your first name is " +firstName+ " and your last name is " +lastName);
		System.out.println("-----------------------------------------------------------------------");
		// The int is called an  integer, which only takes in numeric values. This is also how to assign it 
		int num1 = 15;
		System.out.println(num1);
		System.out.println("-----------------------------------------------------------------------");
		//A char can only be a single character as presented down below, and thats how you assign it. 
		char letter = 'A';
		System.out.println(letter);
		System.out.println("-----------------------------------------------------------------------");
		// A boolean can only be true or false, and down below is how you assign it. 
		boolean male = true;
		System.out.println("You are a male " +male);
		System.out.println("-----------------------------------------------------------------------");
		// A double is almost like a integer but it takes numbers that have decimals 
		double num3 =  2.99; 
		System.out.println(num3);
	}

}
