
public class TurtorialPrinting {

	public static void main(String[] args) {
		//Here is how you print to the console.
		System.out.println("Hello My name is Gavin Nguyen.");
		System.out.println("-------------------------------");
		/*When you run this you will notice that they are on 1 line.
		* Because "System.out.print" is just to print text but 
	    * System.out.println is used to print text and move to the second line. */
		System.out.print("Hello My name is Gavin Nguyen, ");
		System.out.println("what is yours? ");
		System.out.println("-------------------------------");
		/*\n is another way of going to the next line. */
		System.out.println("Hello My name is Gavin Nguyen.\n what is yours?  ");
		System.out.println("-------------------------------");
	}

}