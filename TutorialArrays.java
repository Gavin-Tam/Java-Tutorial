
public class TutorialArrays {

	public static void main(String[] args) {
		System.out.println("Example of an Array");
		
		//Arrays is a list, example
		
		String[] backPack = {"LunchBox", "Laptop", "Binder"};
		                //Index  0          1         2
		System.out.println(backPack [0]);
		System.out.println(backPack [1]);
		System.out.println(backPack [2]);
		System.out.println(backPack.length);// tells you how many items are in your array
		
		int[] grades= { 50,75,68,80,98,100};
		System.out.println(grades [0]);
		System.out.println(grades.length);
		
	}

}
